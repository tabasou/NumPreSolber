        public void getQuestion(int QuestionLv, int testNumber, ref int[,] iGrid)
        {
            switch (QuestionLv)
            {
                case Consts.QUESTION_Lv_Easy:
                    for (int j = 0; j < Consts.WidthMax; j++)
                    {
                        for (int i = 0; i < Consts.HeightMax; i++)
                        {
                            iGrid[j, i] = iQuestions_EasyLv[testNumber, j, i];
                        }
                    }
                    break;
                case Consts.QUESTION_Lv_Normal:
                    for (int j = 0; j < Consts.WidthMax; j++)
                    {
                        for (int i = 0; i < Consts.HeightMax; i++)
                        {
                            iGrid[j, i] = iQuestions_NormalLv[testNumber, j, i];
                        }
                    }
                    break;
                case Consts.QUESTION_Lv_Difficult:
                    for (int j = 0; j < Consts.WidthMax; j++)
                    {
                        for (int i = 0; i < Consts.HeightMax; i++)
                        {
                            iGrid[j, i] = iQuestions_DifficultLv[testNumber, j, i];
                        }
                    }
                    break;
                default:
                    break;

            }
        }
        public int getAnswer(int Lv, int No, int hIdx, int wIdx)
        {
            int rtn = 0;
            switch (Lv)
            {
                case Consts.QUESTION_Lv_Easy:
                    rtn = iAnswer_EasyLv[No, hIdx, wIdx];
                    break;
                case Consts.QUESTION_Lv_Normal:
                    rtn = iAnswer_NormalLv[No, hIdx, wIdx];
                    break;
                case Consts.QUESTION_Lv_Difficult:
                    rtn = iAnswer_DifficultLv[No, hIdx, wIdx];
                    break;
                default:
                    break;
            }
            return rtn;
        }

        public int getQuestionNumMax(int Lv)
        {
            int QuestNum = 0;
            if (Lv == Consts.QUESTION_Lv_Easy)
            {
                QuestNum = iQuestions_EasyLv.Length / Consts.WidthMax / Consts.HeightMax;
            }
            if (Lv == Consts.QUESTION_Lv_Normal)
            {
                QuestNum = iQuestions_NormalLv.Length / Consts.WidthMax / Consts.HeightMax;
            }
            if (Lv == Consts.QUESTION_Lv_Difficult)
            {
                QuestNum = iQuestions_DifficultLv.Length / Consts.WidthMax / Consts.HeightMax;
            }

            return QuestNum;
        }
        
